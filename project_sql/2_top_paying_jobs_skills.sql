
with top_paying_jobs as (
    Select
    j.job_id,
    j.job_title,
    j.salary_year_avg,
    c.name as company_name
FROM job_postings_fact AS j
left JOIN company_dim as c on c.company_id = j.company_id
WHERE
    j.job_title_short = 'Data Analyst' AND
    j.job_location = 'Anywhere' AND
    j.salary_year_avg is NOT NULL
ORDER BY
    j.salary_year_avg DESC
LIMIT 10
)

select top_paying_jobs.*,
    s.skills
from top_paying_jobs
INNER JOIN skills_job_dim as ss on top_paying_jobs.job_id = ss.job_id
INNER JOIN skills_dim as s on ss.skill_id = s.skill_id
ORDER BY
    top_paying_jobs.salary_year_avg

